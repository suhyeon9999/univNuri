package com.university.nuri.vo.teachervo;

public class TeacherVO {
	private String t_idx, user_idx, dept_idx, t_dept_chair, t_active;

	public String getT_idx() {
		return t_idx;
	}

	public void setT_idx(String t_idx) {
		this.t_idx = t_idx;
	}

	public String getUser_idx() {
		return user_idx;
	}

	public void setUser_idx(String user_idx) {
		this.user_idx = user_idx;
	}

	public String getDept_idx() {
		return dept_idx;
	}

	public void setDept_idx(String dept_idx) {
		this.dept_idx = dept_idx;
	}

	public String getT_dept_chair() {
		return t_dept_chair;
	}

	public void setT_dept_chair(String t_dept_chair) {
		this.t_dept_chair = t_dept_chair;
	}

	public String getT_active() {
		return t_active;
	}

	public void setT_active(String t_active) {
		this.t_active = t_active;
	}
}
