package com.university.nuri.vo.teachervo;

public class ObjectionVO {
	private String objection_idx, enroll_idx, objection_content, apply_date, handle_date, approve_objection, objection_active;

	public String getObjection_idx() {
		return objection_idx;
	}

	public void setObjection_idx(String objection_idx) {
		this.objection_idx = objection_idx;
	}

	public String getEnroll_idx() {
		return enroll_idx;
	}

	public void setEnroll_idx(String enroll_idx) {
		this.enroll_idx = enroll_idx;
	}

	public String getObjection_content() {
		return objection_content;
	}

	public void setObjection_content(String objection_content) {
		this.objection_content = objection_content;
	}

	public String getApply_date() {
		return apply_date;
	}

	public void setApply_date(String apply_date) {
		this.apply_date = apply_date;
	}

	public String getHandle_date() {
		return handle_date;
	}

	public void setHandle_date(String handle_date) {
		this.handle_date = handle_date;
	}

	public String getApprove_objection() {
		return approve_objection;
	}

	public void setApprove_objection(String approve_objection) {
		this.approve_objection = approve_objection;
	}

	public String getObjection_active() {
		return objection_active;
	}

	public void setObjection_active(String objection_active) {
		this.objection_active = objection_active;
	}
	
}
