package com.university.nuri.vo.adminvo;

public class SubjectVO {
	private String subject_idx, dept_idx, subject_name, subject_active;

	public String getSubject_idx() {
		return subject_idx;
	}

	public void setSubject_idx(String subject_idx) {
		this.subject_idx = subject_idx;
	}

	public String getDept_idx() {
		return dept_idx;
	}

	public void setDept_idx(String dept_idx) {
		this.dept_idx = dept_idx;
	}

	public String getSubject_name() {
		return subject_name;
	}

	public void setSubject_name(String subject_name) {
		this.subject_name = subject_name;
	}

	public String getSubject_active() {
		return subject_active;
	}

	public void setSubject_active(String subject_active) {
		this.subject_active = subject_active;
	}
}
