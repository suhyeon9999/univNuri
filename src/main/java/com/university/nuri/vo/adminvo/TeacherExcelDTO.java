package com.university.nuri.vo.adminvo;

public class TeacherExcelDTO {
	// 멤버필드
	private String name, birth, email, phone, dept_name, t_dept_chair;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBirth() {
		return birth;
	}

	public void setBirth(String birth) {
		this.birth = birth;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}


	public String getDept_name() {
		return dept_name;
	}

	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}

	public String getT_dept_chair() {
		return t_dept_chair;
	}

	public void setT_dept_chair(String t_dept_chair) {
		this.t_dept_chair = t_dept_chair;
	}
	
	
}
