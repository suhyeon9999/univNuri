package com.university.nuri.vo.commonvo;

public class EnrollVO {
	private String enroll_idx, lect_idx, s_idx, enroll_status, enroll_time;

	public String getEnroll_idx() {
		return enroll_idx;
	}

	public void setEnroll_idx(String enroll_idx) {
		this.enroll_idx = enroll_idx;
	}

	public String getLect_idx() {
		return lect_idx;
	}

	public void setLect_idx(String lect_idx) {
		this.lect_idx = lect_idx;
	}

	public String getS_idx() {
		return s_idx;
	}

	public void setS_idx(String s_idx) {
		this.s_idx = s_idx;
	}

	public String getEnroll_status() {
		return enroll_status;
	}

	public void setEnroll_status(String enroll_status) {
		this.enroll_status = enroll_status;
	}

	public String getEnroll_time() {
		return enroll_time;
	}

	public void setEnroll_time(String enroll_time) {
		this.enroll_time = enroll_time;
	}
	
}
