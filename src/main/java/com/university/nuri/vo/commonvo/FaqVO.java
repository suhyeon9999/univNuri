package com.university.nuri.vo.commonvo;

public class FaqVO {
	private String faq_idx, admin_idx, title, write_date, content, faq_active;

	public String getFaq_idx() {
		return faq_idx;
	}

	public void setFaq_idx(String faq_idx) {
		this.faq_idx = faq_idx;
	}

	public String getAdmin_idx() {
		return admin_idx;
	}

	public void setAdmin_idx(String admin_idx) {
		this.admin_idx = admin_idx;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getWrite_date() {
		return write_date;
	}

	public void setWrite_date(String write_date) {
		this.write_date = write_date;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getFaq_active() {
		return faq_active;
	}

	public void setFaq_active(String faq_active) {
		this.faq_active = faq_active;
	}
	
}
