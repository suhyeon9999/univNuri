package com.university.nuri.vo.commonvo;

public class RequestVO {
	private String req_idx, s_idx, admin_idx, req_type, req_response, req_reason, req_date, req_response_date, req_active;

	public String getReq_idx() {
		return req_idx;
	}

	public void setReq_idx(String req_idx) {
		this.req_idx = req_idx;
	}

	public String getS_idx() {
		return s_idx;
	}

	public void setS_idx(String s_idx) {
		this.s_idx = s_idx;
	}

	public String getAdmin_idx() {
		return admin_idx;
	}

	public void setAdmin_idx(String admin_idx) {
		this.admin_idx = admin_idx;
	}

	public String getReq_type() {
		return req_type;
	}

	public void setReq_type(String req_type) {
		this.req_type = req_type;
	}

	public String getReq_response() {
		return req_response;
	}

	public void setReq_response(String req_response) {
		this.req_response = req_response;
	}

	public String getReq_reason() {
		return req_reason;
	}

	public void setReq_reason(String req_reason) {
		this.req_reason = req_reason;
	}

	public String getReq_date() {
		return req_date;
	}

	public void setReq_date(String req_date) {
		this.req_date = req_date;
	}

	public String getReq_response_date() {
		return req_response_date;
	}

	public void setReq_response_date(String req_response_date) {
		this.req_response_date = req_response_date;
	}

	public String getReq_active() {
		return req_active;
	}

	public void setReq_active(String req_active) {
		this.req_active = req_active;
	}
	
}
