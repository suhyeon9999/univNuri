package com.university.nuri.vo.commonvo;

public class ClassVO {
	private String class_idx, class_building, class_room, class_max;

	public String getClass_idx() {
		return class_idx;
	}

	public void setClass_idx(String class_idx) {
		this.class_idx = class_idx;
	}

	public String getClass_building() {
		return class_building;
	}

	public void setClass_building(String class_building) {
		this.class_building = class_building;
	}

	public String getClass_room() {
		return class_room;
	}

	public void setClass_room(String class_room) {
		this.class_room = class_room;
	}

	public String getClass_max() {
		return class_max;
	}

	public void setClass_max(String class_max) {
		this.class_max = class_max;
	}
	
}
